package lanchedajack;

public class Sobremesa {
    private String nome;
    private String tamanho;

    public Sobremesa( String nome, String tamanho) {
        this.nome = nome;
        this.tamanho = tamanho;
    }

    public String getNome() {
        return nome;
    }

    public String getTamanho() {
        return tamanho;
    }
}
